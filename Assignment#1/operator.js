// Initialize a variable
let x = 5;
// Post-increment operator (x++)
console.log("Initial x:", x);
console.log("Post-incremented x:", x++); 
console.log("After post-increment:", x); 
// Reset x for demonstration
x = 5;
// Pre-increment operator (++x)
console.log("Initial x:", x); 
console.log("Pre-incremented x:", ++x); 
console.log("After pre-increment:", x); 
x = 5;
// Double equal operator (==)
console.log("x == '5':", x == '5'); 
console.log("x == 5:", x == 5); 
// Triple equal operator (===)
console.log("x === '5':", x === '5'); 
console.log("x === 5:", x === 5); 
